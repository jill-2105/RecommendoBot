package pageRanking;

public class Laptop {
    public String brandName;
    public String product;
    public String price;
    public String processor;
    public String memory;
    public String storage;
    public String graphics;
    public String display;
    public String os;
    public String image;

    public Laptop(String brandName, String product, String price, String processor,
            String memory, String storage, String graphics, String display, String os, String image) {
        this.brandName = brandName;
        this.product = product;
        this.price = price;
        this.processor = processor;
        this.memory = memory;
        this.storage = storage;
        this.graphics = graphics;
        this.display = display;
        this.os = os;
        this.image = image;
    }
}
